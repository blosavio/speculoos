[:section#alternatives
 [:h2 "Alternatives"]
 [:ul
  [:li "Staples SparX " [:a {:href "https://github.com/staples-sparx/clj-schema"} "clj-schema"]
   [:p  "Schemas for Clojure data structures and values. Delineates operations on maps, seqs, and sets. Contributors: Alex Baranosky, Laurent Petit, Punit Rathore"]
   [:br]]

  [:li "Steve Miner's " [:a {:href "https://github.com/miner/herbert"} "Herbert"]
   [:p  "A schema language for Clojure data for documenting and validating."]
   [:br]]


  [:li "Metosin " [:a {:href "https://github.com/metosin/malli"} "Malli"]
   [:p "Data-driven schemas incorporating the best parts of existing libs, mixed with their own tools."]
   [:br]]

  [:li "Plumatic " [:a {:href "https://github.com/plumatic/schema"} "Schema"]
   [:p "A Clojure(Script) library for declarative data description and validation."]
   [:br]]

  [:li "Christophe Grand's " [:a {:href "https://github.com/cgrand/seqexp"} "seqexp"]
   [:p "Regular expressions for sequences (and other sequables)." ]
   [:br]]

  [:li "Jonathan Claggett's " [:a {:href "https://github.com/jclaggett/seqex"} "seqex"]
   [:p "Sequence Expressions, similar to regular expressions but able to describe arbitrary sequences of values (not just characters)."]
   [:br]]

  [:li "Clojure's " [:a {:href "https://github.com/clojure/spec.alpha"} [:code "spec.alpha"]]
   [:p "[A] Clojure library to describe the structure of data and functions."]
   [:br]]

  [:li "Clojure's " [:a {:href "https://github.com/clojure/spec-alpha2"} [:code "spec-alpha2"] " or " [:code "alpha.spec"]]
   [:p "[A]n evolution from spec.alpha as well as work towards several new features. Note: Alex Miller considers it "
    [:a {:href "https://ask.clojure.org/index.php/9397/clarify-usage-on-the-spec-alpha2-github-page?show=9398#a9398"} "a work in progress"]
    " as of 2020 June 20."]
   [:br]]

  [:li "Jamie Brandon's " [:a {:href "https://github.com/jamii/strucjure"} "Strucjure"]
   [:p "A " [:a {:href "https://www.scattered-thoughts.net/writing/strucjure-motivation/"} "library for describing stuff"] " in an executable manner."]
   [:br]]

  [:li "Brian Marick's " [:a {:href "https://github.com/marick/structural-typing"} "structural-typing"]
   [:p "A library that provides good error messages when checking the correctness of structures, and a way to define " [:a {:href "https://en.wikipedia.org/wiki/Structural_type_system"} "structural types"] " that are checked at runtime."]
   [:br]]

  [:li "Peter Taoussanis' " [:a {:href "https://github.com/taoensso/truss"} "Truss"]
   [:p "A tiny library that provides fast and flexible runtime assertions with terrific error messages."]
   [:br]]]]